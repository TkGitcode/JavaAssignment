/*29.Program to Compute Quotient and Remainder*/
public class Quo_Rem_29 {

	public static void main(String[] args) {
		int n=100,n1=20;
		int Quo=n/n1;
		int Rem=n%n1;
		System.out.println("Quotient :"+Quo);
		System.out.println("Remainder :"+Rem);
	}

}
